let currentLang = "pl"

window.setLang = function setLang(lang) {
  currentLang = lang
}

window.t = function t(key, vars = {}) {
  const text = LANG[currentLang]?.[key] || LANG["en"]?.[key] || key
  return text.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? `{${k}}`)
}

window.getLang = function getLang() {
  return currentLang
}
